        </p>

        <p>
            Sejak didirikan, Bhumi Rantau Sinergi telah mengembangkan berbagai program dan sistem yang mendukung tata kelola pemerintahan, pemberdayaan ekonomi lokal, serta penguatan kapasitas masyarakat desa.
        </p>

        <p>
